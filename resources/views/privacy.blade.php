        <h2>1. Preâmbulo</h2>
        <p>
            O Regulamento (UE) 2016/679 do Parlamento Europeu e do Conselho de 27 de abril de 2016 relativo à proteção
            das pessoas singulares no que diz respeito ao tratamento de dados pessoais e à livre circulação desses dados
            e que revoga a Diretiva 95/46/CE (Regulamento Geral sobre a Proteção de Dados – RGPD), conjugado com a Lei
            n.º 58/2019, de 8 de Agosto, assegura a execução, na ordem jurídica nacional, do referido Regulamento.
        </p>
        <p>
            No âmbito da sua atividade, os advogados Bruno Medina Monteiro (NIF 221 195 041) e Dafne Bensimon (NIF 241
            614 708), com escritório na Rua Garrett, nº 19 - 2º C, 1200-203 Lisboa, (telefone: +351 218 386 856) /
            e-mail: <a href="mailto:geral@mmba.pt">geral@mmba.pt</a>, procedem ao tratamento dos dados pessoais dos seus
            clientes e demais contactos.
        </p>
        <p>
            Para efeitos desta Política, entende-se por:
        </p>
        <ul>
            <li><strong>Cliente(s):</strong> qualquer pessoa singular ou coletiva cliente dos Advogados; inclui
                colaboradores de clientes (empresas);</li>
            <li><strong>Contacto(s):</strong> qualquer pessoa singular ou coletiva que se relacione com os Advogados sem
                ser cliente (potenciais clientes, parceiros, candidatos...);</li>
            <li><strong>Responsável pelo tratamento:</strong> quem determina os fins e meios do tratamento de dados –
                neste caso, os Advogados;</li>
            <li><strong>Subcontratante:</strong> quem trata os dados por conta dos Advogados – por ex., prestadores de
                serviços usados pelos Advogados;</li>
            <li><strong>Titulares:</strong> as pessoas a quem os dados dizem respeito – neste caso, os "Clientes" ou
                "Contactos";</li>
            <li><strong>Destinatários:</strong> quem recebe dados pessoais, podendo ser entidades externas aos Advogados
                (tribunais, IT, contabilidade...);</li>
        </ul>
        <p>
            Nos termos do disposto no artigo 12.º do RGPD, o responsável pelo tratamento deve prestar aos titulares as
            informações sobre os respetivos direitos de forma concisa, transparente, inteligível e de fácil acesso,
            utilizando uma linguagem clara e simples.
        </p>

        <h2>2. Objeto</h2>
        <p>
            Para satisfazer as necessidades da sua atividade, os Advogados procedem ao tratamento de dados pessoais
            relativos aos seus Clientes e aos seus Contactos.
        </p>
        <p>
            Esta política tem como objetivo assegurar o cumprimento da obrigação de informação que impede sobre os
            Advogados e, bem assim, formalizar os direitos e as obrigações dos seus Clientes e dos seus Contactos no que
            respeita ao tratamento dos seus dados pessoais.
        </p>

        <h2>3. Âmbito</h2>
        <p>
            A presente política de proteção de dados pessoais aplica-se no âmbito do tratamento dos dados pessoais dos
            Titulares.
        </p>
        <p>
            A presente política aplica-se unicamente aos tratamentos relativamente aos quais os Advogados atuam como
            Responsáveis.
        </p>
        <p>
            O tratamento de dados pessoais pode ser assegurado diretamente pelos Advogados ou por intermédio de um
            Subcontratado designado para o efeito.
        </p>

        <h2>4. Princípios gerais e consentimento</h2>
        <p>
            Os Advogados apenas procedem ao tratamento de dados pessoais dos Titulares que tenham sido recolhidos no
            âmbito da prestação dos seus serviços e para efeito da prestação dos referidos serviços, ou que sejam
            processados em relação com os referidos serviços, e sempre no estrito cumprimento dos princípios gerais do
            RGPD.
        </p>
        <p>
            Qualquer outro tratamento, alteração ou eliminação de um tratamento de dados pessoais existente será levado
            ao conhecimento dos Titulares por alteração da presente Política.
        </p>

        <h2>5. Tipos de dados recolhidos</h2>
        <p>Os Advogados podem tratar as seguintes categorias de dados pessoais:</p>

        <p><strong>Dados de carácter não técnico (consoante os casos)</strong></p>
        <ul>
            <li>Identificação: nome, apelido, título, função, pseudónimo, data de nascimento, sexo, número do cartão de
                cidadão, passaporte e número de contribuinte, naturalidade, nacionalidade, estado civil;</li>
            <li>Contactos: telefone, telemóvel, endereço de correio eletrónico (e-mail), morada postal, etc.;</li>
            <li>Educação e experiência profissional: educação, qualificações, certificações, línguas, curricula,
                informações do antigo empregador;</li>
            <li>Dados profissionais: cargo, função, descrição da função, empresa, morada do escritório;</li>
            <li>Dados de atividade profissional: atividades de negócio, informações relativas a processos e dossiers,
                informações relativas a diligências;</li>
            <li>Vida pessoal (familiar ou patrimonial);</li>
            <li>Dados de faturação: dados bancários, honorários, despesas de deslocação e comunicações por conta do
                cliente.</li>
        </ul>

        <p><strong>Dados técnicos (consoante os casos)</strong></p>
        <ul>
            <li>Dados de identificação (IP)</li>
            <li>Dados de ligação (logs designadamente)</li>
        </ul>

        <p>
            Os Advogados não procedem ao tratamento de categorias especiais de dados pessoais, conforme previsto no
            artigo 9.º do RGPD, salvo quando o tratamento for necessário à declaração, ao exercício ou à defesa de um
            direito num processo judicial ou sempre que os tribunais atuem no exercício da sua função jurisdicional, nos
            termos do disposto no artigo 9.º, n.º 2, alínea f) do RGPD.
        </p>

        <h2>6. Origem de dados</h2>
        <p>
            Os Advogados recolhem os dados dos Titulares através das seguintes formas:
        </p>
        <ul>
            <li>Dados fornecidos pelos Titulares no âmbito de um caso, de um processo ou de um dossier entregue aos
                Advogados para que estes assegurem o respetivo patrocínio, aconselhamento, assessoria jurídica;</li>
            <li>Cartões pessoais.</li>
        </ul>
        <p>
            Os dados pessoais dos Titulares podem ser recolhidos por vários meios, designadamente suporte papel, por
            correio eletrónico (e-mail), por correio postal, presencialmente.
        </p>

        <h2>7. Finalidades do tratamento</h2>
        <p>
            Consoante os casos, os Advogados procedem aos tratamentos dos dados pessoais dos Titulares para as seguintes
            finalidades:
        </p>
        <ul>
            <li>Prestação de serviços de advocacia: tratamento dos casos, processos ou dossiers entregues aos Advogados
                para que estes assegurem o respetivo patrocínio, aconselhamento, assessoria jurídica (inclui abertura de
                cliente e dossier, registo das propostas de serviços apresentadas, comunicações com o Cliente, com
                outras partes e/ou entidades públicas, incluindo tribunais, arquivo de documentação em suporte digital e
                em suporte físico);</li>
            <li>Gestão da relação com o Cliente;</li>
            <li>Envio de informações (legal alerts);</li>
            <li>Prestação de informação (por telefone ou por e-mail);</li>
            <li>Processamento contabilístico (inclui contabilização de despesas, controlo de custos e de reembolsos
                (e.g., despesas de deslocação a suportar pelos clientes), faturação e gestão de contas correntes,
                manutenção de arquivo de registos contabilísticos e documentação de suporte);</li>
            <li>Respostas a concursos públicos ou a pedidos de propostas;</li>
            <li>Recrutamento e seleção;</li>
            <li>Envio de votos e outras mensagens de cortesia por parte dos Advogados;</li>
            <li>Melhoria dos nossos serviços;</li>
            <li>Cumprimento das suas obrigações legais, nomeadamente as relativas ao combate ao branqueamento de
                capitais e financiamento do terrorismo (BC-FT);</li>
            <li>Assegurar a segurança da rede e de informação (prestação de assistência técnica, prevenção e deteção de
                fraude, etc.);</li>
            <li>Análise estatística.</li>
        </ul>

        <h2>8. Fundamento de licitude</h2>
        <p>
            As finalidades do tratamento supra referidas têm os seguintes fundamentos de licitude:
        </p>
        <p>
            a) Consentimento do Titular para o tratamento dos seus dados pessoais para uma ou mais finalidades
            específicas;<br>
            b) Tratamento necessário para a execução de um contrato no qual o Titular dos dados é parte, ou para
            diligências pré-contratuais a pedido do Titular dos dados;<br>
            c) Tratamento necessário para o cumprimento de uma obrigação legal a que os Advogados estejam sujeitos;<br>
            d) Tratamento necessário para efeito dos interesses legítimos prosseguidos pelos Advogados ou por terceiros,
            exceto se prevalecerem os interesses ou direitos e liberdades fundamentais do Titular que exijam a proteção
            dos dados pessoais, em especial se o titular for uma criança.
        </p>

        <h2>9. Destinatários dos dados</h2>
        <p>
            Os Advogados garantem que os dados pessoais sejam somente acedidos por destinatários internos ou
            destinatários externos autorizados.
        </p>
        <p>
            Os Advogados podem partilhar os dados pessoais dos Titulares com as seguintes entidades:
        </p>
        <p><strong>Destinatários internos:</strong></p>
        <ul>
            <li>Advogados, incluindo advogados estagiários;</li>
            <li>Pessoal administrativo.</li>
        </ul>
        <p><strong>Destinatários externos:</strong></p>
        <ul>
            <li>Contrapartes;</li>
            <li>Tribunais, autoridades regulatórias, instituições governamentais, agentes de execução, administradores
                judiciais, outros advogados, peritos, inspetores, etc.;</li>
            <li>Autoridades públicas, Ordem dos Advogados;</li>
            <li>Administração pública (Instituto dos Registos e Notariado, Conservatórias do Registo Comercial, Redial,
                Automóvel, Autoridade Tributária, etc.);</li>
            <li>Prestadores de serviços (serviços de tradução, serviços de TI, serviços de comunicações, serviços de
                arquivo digital e físico, etc.).</li>
        </ul>
        <p>
            Nos casos em que a transmissão de dados pessoais para as entidades acima referidas envolva uma transferência
            internacional dos dados pessoais (i.e., para fora da União Europeia), os Advogados:<br>
            (i) realizarão a transferência com base em decisão de adequação da Comissão, nos termos da qual o país ou
            organização internacional em causa garante um nível de proteção de dados pessoais equivalente ao decorrente
            da legislação da União Europeia; ou<br>
            (ii) caso não exista decisão de adequação da Comissão, assegurarão que essas transferências de dados se
            realizam no estrito cumprimento das disposições legais e que são implementadas garantias adequadas para
            assegurar a proteção dos dados pessoais.
        </p>

        <h2>10. Prazos de conservação</h2>
        <p>
            O prazo de conservação dos dados pessoais é definido pelos Advogados em função dos constrangimentos legais e
            contratuais que impõem sobre ela, designadamente os prazos legais de prescrição da responsabilidade civil.
        </p>
        <p>
            De acordo com as finalidades do tratamento, os prazos de conservação dos dados pessoais são os
            seguintes:<br>
            Serviços de advocacia: 20 (vinte) anos após a cessação da relação contratual.<br>
            Cumprimento de obrigações legais em matéria de BC-FT: 7 (sete) anos contados a partir da data em que a
            identificação se processou ou, no caso das relações de negócio, após o termo das mesmas.<br>
            Faturação e gestão contabilística: 10 (dez) anos.<br>
            Recrutamento e seleção: 2 (dois) anos, contados desde a entrega da candidatura ou curriculum, caso não se
            estabeleça qualquer relação com o candidato.
        </p>

        <h2>11. Direito de acesso</h2>
        <p>
            Os Titulares têm o direito de obter dos Advogados a confirmação de que os dados pessoais que lhes digam
            respeito são ou não objeto de tratamento e, se for esse o caso, o direito de aceder aos seus dados pessoais.
        </p>
        <p>
            Os Titulares têm o direito de solicitar aos Advogados uma cópia dos seus dados pessoais em fase de
            tratamento. Para fornecer outras cópias solicitadas pelo titular dos dados, os Advogados poderão exigir o
            pagamento de uma taxa razoável tendo em conta os custos administrativos. Se os Titulares apresentarem o
            pedido por meios eletrónicos, e salvo pedido em contrário, a informação será fornecida em formato
            eletrónico.
        </p>
        <p>
            Os Titulares são informados de que o direito de acesso não pode incidir sobre informações ou dados
            confidenciais ou ainda sobre dados cuja comunicação não é permitida por lei. O direito de acesso não permite
            o acesso a documentos entregues aos Advogados e sujeitos a segredo profissional.
        </p>

        <h2>12. Atualização e Retificação</h2>
        <p>
            Os Titulares têm o direito de obter dos Advogados a retificação dos dados pessoais inexatos que lhes digam
            respeito. Tendo em conta as finalidades do tratamento, os Titulares têm direito a que os seus dados pessoais
            incompletos sejam completados, incluindo por meio de uma declaração adicional.
        </p>
        <p>
            Os Titulares devem exercer este direito junto do seu interlocutor habitual ou, em alternativa, enviando o
            respetivo pedido por e-mail para: geral@mmba.pt.
        </p>
        <p>
            Os Titulares são responsáveis pela atualização dos respetivos dados pessoais junto dos Advogados.
        </p>

        <h2>13. Direito ao apagamento dos dados</h2>
        <p>
            O direito ao apagamento dos dados pessoais dos Titulares não será aplicável quando o tratamento seja
            necessário para cumprimento de uma obrigação legal ou ainda para efeitos de declaração, exercício ou defesa
            de um direito num processo judicial.
        </p>
        <p>
            Com exceção da situação acima referida, os Titulares têm o direito de obter dos Advogados o apagamento dos
            seus dados pessoais, quando se aplique um dos seguintes motivos:
        </p>
        <p>
            a) Os dados pessoais deixaram de ser necessários para a finalidade que motivou a sua recolha ou
            tratamento;<br>
            b) Os Titulares retiram o consentimento em que se baseia o tratamento dos dados e não existe outro
            fundamento de legitimidade para o tratamento dos referidos dados;<br>
            c) Os Titulares opõem-se ao tratamento dos seus dados pessoais efetuado com fundamento nos interesses
            legítimos dos Advogados e não existem interesses legítimos prevalecentes que justifiquem o tratamento.
        </p>

        <h2>14. Direito à limitação do tratamento</h2>
        <p>
            Os Titulares têm o direito de obter dos Advogados a limitação do tratamento, se se aplicar uma das seguintes
            situações:
        </p>
        <p>
            a) Contestar a exatidão dos dados pessoais, durante um período que permita aos Advogados verificar a sua
            exatidão;<br>
            b) O tratamento for ilícito e os Titulares se opuserem ao apagamento dos dados pessoais e solicitar, em
            contrapartida, a limitação da sua utilização;<br>
            c) Os Advogados já não precisarem dos dados pessoais para fins de tratamento, mas esses dados sejam
            requeridos pelos Titulares para efeitos de declaração, exercício ou defesa de um direito num processo
            judicial;<br>
            d) Os Titulares se tiverem oposto ao tratamento, até se verificar que os motivos legítimos dos Advogados
            prevalecem sobre os dos Titulares.
        </p>

        <h2>15. Direito de portabilidade dos dados</h2>
        <p>
            Os Advogados asseguram aos Titulares o direito de portabilidade dos dados pessoais que lhe digam respeito,
            desde que esses dados tenham sido fornecidos diretamente pelo Cliente ou pelo Contacto, consoante aplicável,
            e o tratamento seja baseado no consentimento dos Titulares ou num contrato em que estes sejam parte. Nesse
            caso, os dados pessoais serão comunicados pelos Advogados num formato estruturado, de uso corrente e de
            leitura automática.
        </p>

        <h2>16. Direito de oposição</h2>
        <p>
            Os Titulares têm o direito de se opor a qualquer momento, por motivos relacionados com a sua situação
            particular, ao tratamento dos dados pessoais que lhes digam respeito designadamente aqueles que sejam
            baseados nos interesses legítimos prosseguidos pelos Advogados.
        </p>
        <p>
            Em caso de exercício do direito de oposição pelos Titulares, os Advogados cessarão o tratamento dos dados
            pessoais, a não ser que apresente razões imperiosas e legítimas para esse tratamento que prevaleçam sobre os
            interesses, direitos e liberdades dos Titulares, ou para efeitos de declaração, exercício ou defesa de um
            direito num processo judicial.
        </p>

        <h2>17. Direito a retirar o consentimento</h2>
        <p>
            Se o consentimento for legalmente necessário para o tratamento de dados pessoais, os Titulares dos dados têm
            o direito de retirar o seu consentimento a qualquer momento.
        </p>
        <p>
            A retirada do consentimento não compromete a licitude do tratamento efetuado pelos Advogados com base no
            consentimento previamente dado, nem o tratamento posterior dos mesmos dados, baseado noutro fundamento de
            licitude, como é o caso da execução do contrato ou do cumprimento da obrigação legal a que os Advogados
            estejam sujeitos.
        </p>

        <h2>18. Decisão individual automatizada</h2>
        <p>
            Os Advogados não adotam decisões individuais automatizadas.
        </p>

        <h2>19. Direito post mortem</h2>
        <p>
            Os Titulares podem, nos termos legais aplicáveis, deixar determinadas as condições em que pretendem que
            sejam exercidos nomeadamente os direitos de acesso, retificação e apagamento, após a sua morte, podendo
            ainda determinar a impossibilidade de exercício dos direitos referidos no número anterior.
        </p>
        <p>
            A comunicação de diretrizes específicas post-mortem e o exercício dos direitos pelos Titulares deverão ser
            efetuados por e-mail para o endereço: geral@mmba.pt ou por correio postal para a seguinte morada: Rua
            Garrett, nº 19 - 2º C, 1200-203 Lisboa, acompanhada de cópia do documento de identificação devidamente
            assinada pelos Titulares.
        </p>

        <h2>20. Exercício dos direitos</h2>
        <p>
            Nos termos da legislação aplicável em matéria de proteção de dados pessoais, os direitos supra referidos de
            que beneficiam os Titulares são direitos de natureza individual que só podem ser exercidos pelos
            interessados relativamente aos seus próprios dados pessoais. Para cumprimento desta obrigação, os Advogados
            exigirão a comprovação da identidade dos interessados.
        </p>
        <p>
            Caso os Titulares pretendam exercer qualquer um dos seus direitos, deverá contactar os Advogados, remetendo
            o seu pedido por escrito para a seguinte morada: Rua Garrett, nº 19 - 2º C, 1200-203 Lisboa ou por e-mail:
            geral@mmba.pt. O pedido deve ser apresentado pelo próprio, acompanhado de cópia de um documento de
            identificação válido.
        </p>

        <h2>21. Carácter facultativo ou obrigatório das respostas</h2>
        <p>
            Os Titulares são informados, em cada um dos suportes de recolha, do carácter obrigatório ou facultativo das
            respostas pela inserção de um asterisco.
        </p>
        <p>
            No caso de as respostas serem de cariz obrigatório, os Advogados indicarão aos Titulares quais as
            consequências de não fornecer esses dados.
        </p>

        <h2>22. Direito de utilização</h2>
        <p>
            É conferido aos Advogados pelos Titulares um direito de utilização e de tratamento dos seus dados pessoais
            para as finalidades acima indicadas.
        </p>
        <p>
            No entanto, os dados que sejam produzidos pelos Advogados a partir dos dados pessoais originários, fruto de
            um processamento e análise realizados pelos Advogados, são propriedade exclusiva destes (análise,
            estatísticas, etc.).
        </p>

        <h2>23. Subcontratação</h2>
        <p>
            Advogados poderão recorrer a qualquer Subcontratante da sua escolha no âmbito do tratamento dos dados
            pessoais dos Titulares.
        </p>
        <p>
            Caso recorram a Subcontratados, os Advogados garantirão o cumprimento pelo Subcontratado das suas obrigações
            decorrentes do RGPD e demais legislação aplicável.
        </p>
        <p>
            Os Advogados comprometem-se a celebrar com os seus Subcontratados um contrato escrito nos termos do qual
            estes se comprometem ao cumprimento das mesmas obrigações que os Advogados. Para além disso, os Advogados
            reservam-se o direito de conduzir auditorias e/ou inspeções, diretamente ou por intermédio de terceiro
            mandatado para o efeito, junto dos Subcontratantes, com vista à verificação do cumprimento das obrigações
            que impõem sobre estes últimos.
        </p>

        <h2>24. Segurança e Confidencialidade</h2>
        <p>
            Tendo em conta as técnicas mais avançadas, os custos de aplicação e a natureza, o âmbito, o contexto e as
            finalidades do tratamento, bem como os riscos, de probabilidade e gravidade variável, para os direitos e
            liberdades dos Titulares, os Advogados definiram e implementaram as medidas técnicas e organizativas
            adequadas para assegurar um nível de segurança adequado, para acautelar designadamente a destruição, perda e
            alteração acidentais ou ilícitas, e a divulgação ou o acesso não autorizados, de dados pessoais
            transmitidos, conservados ou sujeitos a qualquer outro tipo de tratamento.
        </p>
        <p>
            As medidas adotadas pelos Advogados compreendem designadamente:
        </p>
        <ul>
            <li>A utilização de medidas de segurança no acesso físico às instalações;</li>
            <li>A segurança no acesso aos computadores e telemóveis (alteração regular do código de acesso);</li>
            <li>Login e password para todas as aplicações profissionais;</li>
            <li>Gestão dos acessos ao servidor informático (acessos limitados em função das áreas de atividade
                (departamento financeiro e contabilidade));</li>
            <li>Acesso por VPN nas ligações à distância;</li>
            <li>Palavra-passe complexa para acesso à rede WIFI do escritório, alterada regularmente.</li>
        </ul>
        <p>
            Para este efeito, os Advogados recorrem a um prestador de serviços externo, podendo ainda solicitar a
            qualquer entidade terceira à sua escolha, com a frequência que entender, a realização de auditorias de
            vulnerabilidade ou de testes de intrusão.
        </p>
        <p>
            No âmbito da subcontratação de parte ou da totalidade do tratamento de dados pessoais, os Advogados
            comprometem-se a exigir contratualmente que os Subcontratados assegurem a segurança dos dados pessoais
            mediante a implementação de medidas técnicas de proteção desses dados e a existência de recursos humanos
            adequados.
        </p>
        <p>
            Os Advogados respeitam também a confidencialidade dos dados pessoais que recolhem no âmbito da sua
            atividade. Como tal, não vendem, distribuem ou de qualquer outro modo disponibilizam comercialmente a
