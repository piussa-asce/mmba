            <h2>1. Preamble</h2>
            <p>
                Regulation (EU) 2016/679 of the European Parliament and of the Council of April 27, 2016, regarding the protection
                of natural persons with regard to the processing of personal data and the free movement of such data, which
                repeals Directive 95/46/EC (General Data Protection Regulation – GDPR), in conjunction with Law No. 58/2019, of
                August 8, ensures the implementation of the aforementioned Regulation in national legal order.
            </p>
            <p>
                In the scope of their activities, lawyers Bruno Medina Monteiro (NIF 221 195 041) and Dafne Bensimon (NIF 241 614 708),
                with offices at Rua Garrett, nº 19 - 2º C, 1200-203 Lisbon, (phone: +351 218 386 856) / email:
                <a href="mailto:geral@mmba.pt">geral@mmba.pt</a>, process the personal data of their clients and other contacts.
            </p>
            <p>
                For the purposes of this Policy, the following terms are understood as:
            </p>
            <ul>
                <li><strong>Client(s):</strong> any individual or entity who is a client of the Lawyers; includes client employees (companies);</li>
                <li><strong>Contact(s):</strong> any individual or entity who interacts with the Lawyers without being a client (potential clients, partners, candidates...);</li>
                <li><strong>Data Controller:</strong> the party who determines the purposes and means of data processing – in this case, the Lawyers;</li>
                <li><strong>Subcontractor:</strong> the party who processes data on behalf of the Lawyers – e.g., service providers used by the Lawyers;</li>
                <li><strong>Data Subjects:</strong> the individuals to whom the data pertains – in this case, the "Clients" or "Contacts";</li>
                <li><strong>Recipients:</strong> the parties who receive personal data, which may include external entities to the Lawyers (courts, IT, accounting...);</li>
            </ul>

            <p>
                In accordance with Article 12 of the GDPR, the data controller must provide data subjects with information about
                their rights in a concise, transparent, intelligible, and easily accessible manner, using clear and simple language.
            </p>

            <h2>2. Object</h2>
            <p>
                To meet the needs of their activities, the Lawyers process personal data relating to their Clients and Contacts.
            </p>
            <p>
                This policy aims to ensure compliance with the information obligation imposed on the Lawyers and also to formalize
                the rights and obligations of their Clients and Contacts regarding the processing of their personal data.
            </p>

            <h2>3. Scope</h2>
            <p>
                This personal data protection policy applies to the processing of personal data of the Data Subjects.
            </p>
            <p>
                This policy applies solely to the processing activities for which the Lawyers act as the Data Controllers.
            </p>
            <p>
                The processing of personal data may be carried out directly by the Lawyers or through a designated Subcontractor.
            </p>

            <h2>4. General Principles and Consent</h2>
            <p>
                The Lawyers only process personal data of the Data Subjects that have been collected in the context of providing
                their services and for the purpose of providing those services, or that are processed in relation to those services,
                and always in strict compliance with the general principles of the GDPR.
            </p>
            <p>
                Any other processing, modification, or deletion of existing personal data will be communicated to the Data Subjects
                by updating this Policy.
            </p>

            <h2>5. Types of Data Collected</h2>

            <p>The Lawyers may process the following categories of personal data:</p>

            <p><strong>Non-technical data (depending on the case)</strong></p>
            <ul>
                <li>Identification: name, surname, title, role, pseudonym, date of birth, gender, citizen card number, passport and tax identification number, place of birth, nationality, marital status;</li>
                <li>Contact details: phone, mobile, email address, postal address, etc.;</li>
                <li>Education and professional experience: education, qualifications, certifications, languages, CVs, former employer information;</li>
                <li>Professional data: position, role, job description, company, office address;</li>
                <li>Professional activity data: business activities, information related to cases and files, information related to procedures;</li>
                <li>Personal life (family or financial);</li>
                <li>Billing data: bank details, fees, travel expenses, and communications on behalf of the client.</li>
            </ul>

            <p><strong>Technical data (depending on the case)</strong></p>
            <ul>
                <li>Identification data (IP)</li>
                <li>Connection data (such as logs)</li>
            </ul>

            <p>
                The Lawyers do not process special categories of personal data, as provided in Article 9 of the GDPR, unless the processing
                is necessary for the declaration, exercise, or defense of a right in judicial proceedings or whenever the courts act in the
                exercise of their judicial function, as outlined in Article 9(2)(f) of the GDPR.
            </p>


            <h2>6. Data Sources</h2>

            <p>
                The Lawyers collect data from the Data Subjects through the following methods:
            </p>

            <ul>
                <li>Data provided by the Data Subjects in the context of a case, process, or file submitted to the Lawyers for legal representation, advice, and consultancy;</li>
                <li>Personal identification cards.</li>
            </ul>

            <p>
                The personal data of the Data Subjects may be collected through various means, including paper records, email, postal mail, or in person.
            </p>

            <h2>7. Purposes of Processing</h2>

            <p>
                Depending on the case, the Lawyers process the personal data of the Data Subjects for the following purposes:
            </p>

            <ul>
                <li>Provision of legal services: processing of cases, processes, or files submitted to the Lawyers for legal representation, advice, and consultancy (including client and file creation, registration of service proposals, communications with the client, other parties, and/or public entities, including courts, digital and physical file documentation);</li>
                <li>Client relationship management;</li>
                <li>Sending information (legal alerts);</li>
                <li>Providing information (by phone or email);</li>
                <li>Accounting processing (including expense recording, cost control, and reimbursements (e.g., travel expenses to be covered by clients), invoicing, and management of current accounts, maintaining accounting records and supporting documentation);</li>
                <li>Responses to public tenders or requests for proposals;</li>
                <li>Recruitment and selection;</li>
                <li>Sending greetings and other courtesy messages from the Lawyers;</li>
                <li>Improvement of our services;</li>
                <li>Compliance with legal obligations, including those related to anti-money laundering and counter-terrorist financing (AML-CTF);</li>
                <li>Ensuring network and information security (technical assistance, fraud prevention and detection, etc.);</li>
                <li>Statistical analysis.</li>
            </ul>


            <h2>8. Lawful Basis for Processing</h2>

            <p>
                The purposes of the processing mentioned above are based on the following lawful grounds:
            </p>

            <p>
                a) Consent of the Data Subject for the processing of their personal data for one or more specific purposes;<br>
                b) Processing necessary for the performance of a contract to which the Data Subject is a party, or for pre-contractual steps at the request of the Data Subject;<br>
                c) Processing necessary for compliance with a legal obligation to which the Lawyers are subject;<br>
                d) Processing necessary for the legitimate interests pursued by the Lawyers or third parties, except where such interests are overridden by the interests or fundamental rights and freedoms of the Data Subject that require protection of personal data, especially where the Data Subject is a child.
            </p>

            <h2>9. Data Recipients</h2>

            <p>
                The Lawyers ensure that personal data is only accessed by authorized internal recipients or external recipients.
            </p>

            <p>
                The Lawyers may share the personal data of the Data Subjects with the following entities:
            </p>

            <p><strong>Internal Recipients:</strong></p>
            <ul>
                <li>Lawyers, including trainee lawyers;</li>
                <li>Administrative staff.</li>
            </ul>

            <p><strong>External Recipients:</strong></p>
            <ul>
                <li>Counterparties;</li>
                <li>Courts, regulatory authorities, government institutions, enforcement agents, judicial administrators, other lawyers, experts, inspectors, etc.;</li>
                <li>Public authorities, Bar Association;</li>
                <li>Public administration (Institute of Registers and Notary, Commercial Register, Redial, Motor Vehicle Register, Tax Authority, etc.);</li>
                <li>Service providers (translation services, IT services, communication services, digital and physical archiving services, etc.).</li>
            </ul>

            <p>
                In cases where the transfer of personal data to the entities mentioned above involves an international transfer of personal data (i.e., outside the European Union), the Lawyers:<br>
                (i) will carry out the transfer based on a decision of adequacy from the European Commission, under which the country or international organization in question ensures an adequate level of personal data protection equivalent to that provided by European Union law; or<br>
                (ii) if no adequacy decision from the Commission exists, will ensure that such data transfers are made in strict compliance with legal provisions and that appropriate safeguards are implemented to ensure the protection of personal data.
            </p>

            <h2>10. Retention Periods</h2>

            <p>
                The retention period for personal data is determined by the Lawyers based on legal and contractual constraints, specifically the statutory limitation periods for civil liability.
            </p>

            <p>
                In accordance with the purposes of processing, the retention periods for personal data are as follows:<br>
                Legal services: 20 (twenty) years after the termination of the contractual relationship.<br>
                Compliance with legal obligations related to AML-CTF: 7 (seven) years from the date the identification was processed or, in the case of business relationships, after the end of such relationships.<br>
                Billing and accounting management: 10 (ten) years.<br>
                Recruitment and selection: 2 (two) years from the submission of the application or curriculum vitae, if no relationship is established with the candidate.
            </p>


            <h2>11. Right of Access</h2>
            <p>
                Data Subjects have the right to obtain confirmation from the Lawyers as to whether or not their personal data is being processed, and if so, the right to access their personal data.
            </p>
            <p>
                Data Subjects have the right to request a copy of their personal data being processed. For additional copies requested by the data subject, the Lawyers may charge a reasonable fee taking into account administrative costs. If the Data Subjects request the information electronically, unless otherwise requested, the information will be provided in electronic format.
            </p>
            <p>
                Data Subjects are informed that the right of access does not apply to confidential information or data, nor to data whose communication is prohibited by law. The right of access does not entitle access to documents provided to the Lawyers that are subject to professional secrecy.
            </p>

            <h2>12. Updating and Rectification</h2>
            <p>
                Data Subjects have the right to obtain the rectification of inaccurate personal data concerning them from the Lawyers. Given the purposes of processing, Data Subjects have the right to complete incomplete personal data, including by means of an additional declaration.
            </p>
            <p>
                Data Subjects should exercise this right through their usual contact person or, alternatively, by sending the respective request by email to: geral@mmba.pt.
            </p>
            <p>
                Data Subjects are responsible for updating their personal data with the Lawyers.
            </p>

            <h2>13. Right to Erasure</h2>
            <p>
                The right to erasure of personal data does not apply where the processing is necessary for compliance with a legal obligation or for the declaration, exercise, or defense of a legal claim in court.
            </p>
            <p>
                Except for the situation mentioned above, Data Subjects have the right to obtain the erasure of their personal data from the Lawyers when one of the following reasons applies:
            </p>
            <p>
                a) The personal data is no longer necessary for the purposes for which it was collected or processed;<br>
                b) The Data Subjects withdraw their consent on which the processing is based, and there is no other legal basis for the processing of such data;<br>
                c) The Data Subjects object to the processing of their personal data based on the legitimate interests of the Lawyers, and there are no overriding legitimate grounds for the processing.
            </p>

            <h2>14. Right to Restriction of Processing</h2>
            <p>
                Data Subjects have the right to obtain from the Lawyers the restriction of processing if one of the following situations applies:
            </p>
            <p>
                a) To contest the accuracy of the personal data, for a period that allows the Lawyers to verify its accuracy;<br>
                b) The processing is unlawful, and the Data Subjects oppose the erasure of personal data and request, instead, the restriction of its use;<br>
                c) The Lawyers no longer need the personal data for the purposes of processing, but such data is required by the Data Subjects for the declaration, exercise, or defense of a legal claim in court;<br>
                d) The Data Subjects have objected to processing, until it is verified that the legitimate reasons of the Lawyers override those of the Data Subjects.
            </p>

            <h2>15. Right to Data Portability</h2>
            <p>
                The Lawyers ensure that Data Subjects have the right to data portability of their personal data, provided that such data was provided directly by the Client or Contact, as applicable, and the processing is based on the consent of the Data Subjects or a contract in which they are a party. In this case, the personal data will be provided by the Lawyers in a structured, commonly used, and machine-readable format.
            </p>

            <h2>16. Right to Object</h2>
            <p>
                Data Subjects have the right to object at any time, for reasons related to their particular situation, to the processing of personal data concerning them, especially if based on the legitimate interests pursued by the Lawyers.
            </p>
            <p>
                In the event that the Data Subjects exercise their right to object, the Lawyers will cease processing their personal data, unless they present compelling legitimate grounds for the processing that override the interests, rights, and freedoms of the Data Subjects, or for the declaration, exercise, or defense of a legal claim in court.
            </p>

            <h2>17. Right to Withdraw Consent</h2>
            <p>
                If consent is legally required for the processing of personal data, the Data Subjects have the right to withdraw their consent at any time.
            </p>
            <p>
                The withdrawal of consent does not affect the lawfulness of the processing carried out by the Lawyers based on the consent previously given, nor the subsequent processing of the same data based on another lawful basis, such as the performance of a contract or compliance with a legal obligation to which the Lawyers are subject.
            </p>

            <h2>18. Automated Individual Decision-Making</h2>
            <p>
                The Lawyers do not make automated individual decisions.
            </p>

            <h2>19. Post-Mortem Rights</h2>
            <p>
                Data Subjects may, under applicable law, specify the conditions under which they wish the rights of access, rectification, and erasure to be exercised after their death, and may also determine that the exercise of the rights referred to in the previous paragraph is not permitted.
            </p>
            <p>
                Communication of specific post-mortem guidelines and the exercise of rights by Data Subjects should be made by email to: geral@mmba.pt or by mail to the following address: Rua Garrett, nº 19 - 2º C, 1200-203 Lisbon, accompanied by a signed copy of the identity document of the Data Subjects.
            </p>

            <h2>20. Exercising the Rights</h2>
            <p>
                In accordance with applicable data protection legislation, the above rights of the Data Subjects are individual rights that can only be exercised by the concerned individuals with respect to their own personal data. To comply with this obligation, the Lawyers will require proof of the identity of the individuals.
            </p>
            <p>
                If the Data Subjects wish to exercise any of their rights, they should contact the Lawyers by submitting their request in writing to the following address: Rua Garrett, nº 19 - 2º C, 1200-203 Lisbon or by email: geral@mmba.pt. The request must be made by the individual and accompanied by a copy of a valid identification document.
            </p>


            <h2>21. Mandatory or Optional Nature of Responses</h2>
            <p>
                Data Subjects are informed, on each data collection medium, of the mandatory or optional nature of the responses by the insertion of an asterisk.
            </p>
            <p>
                In the case of mandatory responses, the Lawyers will inform Data Subjects of the consequences of not providing such data.
            </p>

            <h2>22. Right of Use</h2>
            <p>
                The Data Subjects grant the Lawyers the right to use and process their personal data for the purposes mentioned above.
            </p>
            <p>
                However, any data produced by the Lawyers from the original personal data, as a result of processing and analysis performed by the Lawyers, is the exclusive property of the Lawyers (e.g., analysis, statistics, etc.).
            </p>

            <h2>23. Subcontracting</h2>
            <p>
                The Lawyers may use any subcontractor of their choice in the processing of the personal data of the Data Subjects.
            </p>
            <p>
                If subcontractors are used, the Lawyers will ensure the subcontractor complies with the obligations arising from the GDPR and other applicable laws.
            </p>
            <p>
                The Lawyers commit to entering into a written contract with their subcontractors under which the subcontractors agree to comply with the same obligations as the Lawyers. Furthermore, the Lawyers reserve the right to conduct audits and/or inspections, either directly or through a third-party authorized for this purpose, at the subcontractors' premises, to verify compliance with the obligations imposed on them.
            </p>

            <h2>24. Security and Confidentiality</h2>
            <p>
                Considering the most advanced techniques, implementation costs, and the nature, scope, context, and purposes of the processing, as well as the risks, of varying probability and severity, to the rights and freedoms of the Data Subjects, the Lawyers have defined and implemented appropriate technical and organizational measures to ensure an adequate level of security, specifically to prevent accidental or unlawful destruction, loss, alteration, and unauthorized disclosure or access to personal data transmitted, stored, or subject to any other type of processing.
            </p>
            <p>
                The measures adopted by the Lawyers include, but are not limited to:
            </p>
            <ul>
                <li>Use of security measures for physical access to the premises;</li>
                <li>Security in access to computers and mobile phones (regular password changes);</li>
                <li>Login and password for all professional applications;</li>
                <li>Access management to the computer server (limited access based on activity areas such as the finance and accounting departments);</li>
                <li>VPN access for remote connections;</li>
                <li>Complex password for access to the office's WIFI network, regularly changed.</li>
            </ul>
            <p>
                To this end, the Lawyers rely on an external service provider and may request, at their discretion, third-party entities to perform vulnerability audits or penetration tests as frequently as they deem necessary.
            </p>
            <p>
                In the context of subcontracting part or all of the personal data processing, the Lawyers commit to contractually requiring subcontractors to ensure the security of personal data by implementing technical protection measures and ensuring adequate human resources.
            </p>
            <p>
                The Lawyers also respect the confidentiality of the personal data they collect as part of their activities. As such, they do not sell, distribute, or otherwise commercially disclose Data Subjects' information to any third party. The Lawyers undertake to keep the Data Subjects' information confidential in accordance with this Policy and applicable legislation.
            </p>

            <h2>25. Data Breach</h2>
            <p>
                In the event of a data breach, the Lawyers commit to notifying the National Data Protection Commission (CNPD) of the breach, whenever possible, within 72 hours of becoming aware of it, unless the breach is unlikely to result in a risk to the rights and freedoms of the Data Subjects.
            </p>
            <p>
                When the data breach is likely to result in a high risk to the rights and freedoms of the Data Subjects, the Lawyers commit to notifying the Data Subjects of the data breach, including a description of the nature of the breach and providing the following information:
            </p>
            <ul>
                <li>Name and contact details of the contact point where further information can be obtained;</li>
                <li>Description of the likely consequences of the personal data breach;</li>
                <li>Description of the measures taken or proposed by the Lawyers to remedy the data breach, including, if applicable, measures to mitigate its potential negative effects.</li>
            </ul>

